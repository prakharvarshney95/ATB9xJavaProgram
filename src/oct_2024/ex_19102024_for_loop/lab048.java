package oct_2024.ex_19102024_for_loop;

public class lab048 {
    public static void main(String[] args) {
        for (int rohit = 0; rohit < 10; rohit++) {    //10
            System.out.println(rohit);
            //rohit -> 0, 9 - 10 times
        }

        for (int pramod = 1; pramod <= 10; pramod++) {    //10
            System.out.println(pramod);
            // pramod -> 1 to 10, -> 10 times
        }
    }
}
